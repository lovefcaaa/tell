func BaseSignYHKP(deskey string,origData []byte, timestampStr string) (string, string) {
	keyByte := []byte(deskey)
	crypted, err := TripleDesEncrypt(origData, keyByte)
	if err != nil {
		fmt.Println(err)
	}

	var base64Encode string
	base64Encode = base64.StdEncoding.EncodeToString(crypted)

	signPars := "data=" + base64Encode + "&mess=" + deskey + "&timestamp=" + timestampStr + "&key=" + config.Signkey
	sig := hmac.New(sha256.New, []byte(config.Signkey))
	sig.Write([]byte(signPars))
	calcMac := hex.EncodeToString(sig.Sum(nil))
	return base64Encode, calcMac
}

//AesDecrypt des解密方法
func AesDecrypt(crypted, key []byte) ([]byte, error) {
	block, err := aes.NewCipher(key)
	if err != nil {
		return nil, err
	}
	blockSize := block.BlockSize()
	blockMode := cipher.NewCBCDecrypter(block, key[:blockSize])
	origData := make([]byte, len(crypted))
	// origData := crypted
	blockMode.CryptBlocks(origData, crypted)
	origData = PKCS5UnPadding(origData)
	// origData = ZeroUnPadding(origData)
	return origData, nil
}

